    private String printReport(Report rp) {
        String re = rp.getName() + ":\n\t";
        int temp = 0;
        for (String i : rp.getReport().split(" ")) {
            re += i + " ";
            temp++;
            if (temp > 25) {
                temp = 0;
                re += "\n\t";
            }
        }
        return re;
    }

    private void printAllReports() {
        String print = "";
        for (Report i : new DBConnection().getAllReports()) {
            print += printReport(i);
            print += "\n\n";
        }

        textReports.setText(print);
    }

